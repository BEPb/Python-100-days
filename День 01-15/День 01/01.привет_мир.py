# '!' соединил со следующей строкой, для того что бы не было такого необходимо использовать \n
print('следующая строка')  # выводит текст 'следующая строка'
print('Прощай, мир', end='!\n')  # выводит текст 'goodbye, world!', при этом следующая строка уже не прилипает
print('следующая строка')  # выводит текст 'следующая строка'
